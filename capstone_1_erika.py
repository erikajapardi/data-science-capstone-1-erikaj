#Dipakai di Create
def generate_lead_id(dealer_name):
    dealer_code = dealer_codes.get(dealer_name)

    if not dealer_code:
        return None

    max_number = 0

    for lead in leads:
        if lead["dealer"] == dealer_name:
            lead_id = lead["id"]
            number_part = lead_id.split("-")[1]
            number = int(number_part)

            if number > max_number:
                max_number = number

    new_id = f"{dealer_code}-{max_number + 1:03}"
    return new_id

