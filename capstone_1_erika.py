#Additional Feature
def show_priority_leads():
    if not leads:
        print("No leads available.")
        return

    sorted_leads = sorted(leads, key=calculate_lead_score, reverse=True)

    print("\n===== PRIORITY LEADS =====")
    for lead in sorted_leads:
        print(
            f"{lead['id']} | {lead['customer_name']} | "
            f"{lead['dealer']} | Score: {calculate_lead_score(lead)} | "
            f"Promo: {recommend_promo(lead)}"
        )


