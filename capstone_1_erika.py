def calculate_lead_score(lead):
    status_scores = {
        "Assigned": 10,
        "Contacted": 20,
        "Follow Up": 30,
        "Test Drive": 40,
        "SPK": 50,
        "Lost": 0
    }

    return status_scores.get(lead["status"], 0)

#Dipakai di Helper Show Lead, Add Feat Show Priority Leads
def recommend_promo(lead):
    if lead["status"] == "Test Drive":
        return "Offer test drive follow-up promo"
    elif lead["status"] == "Follow Up":
        return "Offer limited-time cashback"
    elif lead["car_model"] in ["Honda Brio", "Honda WR-V", "Honda BR-V"]:
        return "Offer first buyer package / accessory package"
    elif lead["car_model"] in ["Honda CR-V", "Honda Accord", "Honda Civic", "Honda Prelude"]:
        return "Offer premium service package"
    else:
        return "Offer general seasonal promo"

#Dipakai di Read
def shorten_text(text, max_length):
    if len(text) <= max_length:
        return text
    return text[:max_length - 3] + "..."

